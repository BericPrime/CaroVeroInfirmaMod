package net.dengusprime.caroveroinfirma.item;

import net.dengusprime.caroveroinfirma.CaroVeroInfirma;
import net.minecraft.core.registries.Registries;
import net.minecraft.network.chat.Component;
import net.minecraft.world.item.CreativeModeTab;
import net.minecraft.world.item.Item;
import net.minecraft.world.item.ItemStack;
import net.minecraftforge.eventbus.api.IEventBus;
import net.minecraftforge.registries.DeferredRegister;
import net.minecraftforge.registries.RegistryObject;

public class ModCreativeTabs {
    public static final DeferredRegister<CreativeModeTab> CREATIVE_MODE_TAB = DeferredRegister.create(Registries.CREATIVE_MODE_TAB, CaroVeroInfirma.MODID);

    public static final RegistryObject<CreativeModeTab> CAROVEROINFIRMA_ITEMS_TAB = CREATIVE_MODE_TAB.register("caroveroinfirma_items_tab", ()-> CreativeModeTab.builder()
            .icon(()-> new ItemStack(ModItems.BIOBRASS.get()))
            .title(Component.translatable("creativetab.caroveroinfirma_items_tab"))
            .displayItems((pParameters, pOutput) -> {
                for(RegistryObject<Item> item : ModItems.ITEMS.getEntries()) {
                    pOutput.accept(item.get());
                }
            })
            .build())
            ;
    public static void register(IEventBus eventBus){
        CREATIVE_MODE_TAB.register(eventBus);
    }
}
